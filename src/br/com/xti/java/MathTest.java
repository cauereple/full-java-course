package br.com.xti.java;

public class MathTest {

	public static void main(String[] args) {
		
		System.out.println(Math.sin(10)); // seno
		System.out.println(Math.cos(10)); // cosseno
		System.out.println(Math.tan(45)); // tangente
		System.out.println(Math.pow(4, 2)); // potência
		System.out.println(Math.sqrt(16)); // raiz quadrada
		System.out.println(Math.cbrt(16)); // raiz cúbica
		System.out.println(Math.floor(11));
		System.out.println(Math.round(16.5));
		System.out.println(Math.max(16, 18));
		System.out.println(Math.min(16, 18));
	}

}
